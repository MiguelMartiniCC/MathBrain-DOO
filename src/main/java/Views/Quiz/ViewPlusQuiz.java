    private void nextQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextQuestionActionPerformed
        String userAnswer = userInput.getText();
        
        try{
            int userResult = Integer.parseInt(userAnswer);

            if (userResult == (value1 + value2)) {
                correctAnswers++;
                errorMsg.setForeground(new java.awt.Color(0, 153, 0));
                errorMsg.setText("Correto!");
            } else {
                errorMsg.setForeground(new java.awt.Color(204, 0, 0));
                errorMsg.setText("Incorreto! Resposta correta: " + (value1 + value2));
            }

            currentQuestion++;
            
//            
                if (currentQuestion <= totalQuestions) {
                java.util.Timer timer = new java.util.Timer();
                timer.schedule(new java.util.TimerTask() {
                    @Override
                    public void run() {
                        generateNewQuestion();
                    }
                }, 1000); 
            } else {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Quiz Finalizado!\nVocê acertou " + correctAnswers + " de " + totalQuestions + " questões.",
                        "Resultado",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);
                ViewMenu menu = new ViewMenu(user);
                menu.setVisible(true);
                this.dispose(); 
            }
        }catch (NumberFormatException e) {
            errorMsg.setForeground(new java.awt.Color(204, 0, 0)); 
            errorMsg.setText("Por favor, insira um número válido.");
        }
    }//GEN-LAST:event_nextQuestionActionPerformed

